// ============================================================================
//  board_pins.h — Распиновка LILYGO T-Embed CC1101 Plus S3 (ESP32-S3)
//
//  Источник: проверенная конфигурация прошивки Bruce
//  (boards/lilygo-t-embed-cc1101/pins_arduino.h, ветка T_EMBED_1101).
//  Сверено с рабочей прошивкой на устройстве.
//
//  Важно: дисплей, SD-карта и CC1101 СИДЯТ НА ОДНОЙ SPI-шине (HSPI):
//         MOSI 9 / MISO 10 / SCK 11 — различаются только линии CS.
// ============================================================================
#pragma once

// ---------------------------------------------------------------------------
//  Питание / общая логика
// ---------------------------------------------------------------------------
#define PIN_POWER_ON        15      // Удержание питания платы (LCD + батарея)

// ---------------------------------------------------------------------------
//  Общая шина SPI (HSPI): дисплей + SD + CC1101
// ---------------------------------------------------------------------------
#define PIN_SPI_MOSI        9
#define PIN_SPI_MISO        10
#define PIN_SPI_SCLK        11

// ---------------------------------------------------------------------------
//  Шина I2C: топливомер BQ27220, Grove-порт
// ---------------------------------------------------------------------------
#define PIN_I2C_SDA         8
#define PIN_I2C_SCL         18

// ---------------------------------------------------------------------------
//  Дисплей ST7789 (170 x 320), на общей шине SPI
// ---------------------------------------------------------------------------
#define BOARD_TFT_WIDTH     170
#define BOARD_TFT_HEIGHT    320
#define BOARD_TFT_OFFSET_X  35      // Смещение активной области панели
#define BOARD_TFT_OFFSET_Y  0

#define PIN_LCD_BL          21      // Подсветка (PWM)
#define PIN_LCD_DC          16
#define PIN_LCD_CS          41
#define PIN_LCD_SCLK        PIN_SPI_SCLK
#define PIN_LCD_MOSI        PIN_SPI_MOSI
#define PIN_LCD_MISO        -1      // Панель не читается
#define PIN_LCD_RST         40

// ---------------------------------------------------------------------------
//  Энкодер вращения + кнопки
// ---------------------------------------------------------------------------
#define PIN_ENCODER_A       4
#define PIN_ENCODER_B       5
#define PIN_ENCODER_BTN     0       // Нажатие энкодера (активный LOW)
#define PIN_BTN_BACK        6       // Боковая кнопка / пробуждение из сна (LOW)

// ---------------------------------------------------------------------------
//  Радиомодуль CC1101 (sub-GHz). Общая шина SPI, отдельный CS + GDO + RF-switch.
//  SW0/SW1 управляют антенным переключателем (приём/передача) — обязательны.
// ---------------------------------------------------------------------------
#define PIN_CC1101_SCLK     PIN_SPI_SCLK
#define PIN_CC1101_MISO     PIN_SPI_MISO
#define PIN_CC1101_MOSI     PIN_SPI_MOSI
#define PIN_CC1101_CS       12
#define PIN_CC1101_GDO0     3
#define PIN_CC1101_GDO2     38
#define PIN_CC1101_SW1      47
#define PIN_CC1101_SW0      48

// ---------------------------------------------------------------------------
//  SD-карта (общая шина SPI, отдельный CS)
// ---------------------------------------------------------------------------
#define PIN_SD_CS           13

// ---------------------------------------------------------------------------
//  Инфракрасный приёмопередатчик (активный уровень — HIGH)
// ---------------------------------------------------------------------------
#define PIN_IR_TX           2
#define PIN_IR_RX           1

// ---------------------------------------------------------------------------
//  NFC/RFID 13.56 МГц: PN532 по I2C (общая шина SDA 8 / SCL 18)
// ---------------------------------------------------------------------------
#define PN532_IRQ           17
#define PN532_RF_REST       45

// ---------------------------------------------------------------------------
//  Батарея: топливомер BQ27220 по I2C (НЕ простой ADC)
// ---------------------------------------------------------------------------
#define BQ27220_I2C_ADDRESS 0x55

// ---------------------------------------------------------------------------
//  Адресный светодиод (WS2812B x8)
// ---------------------------------------------------------------------------
#ifdef PIN_NEOPIXEL                 // вариант ESP32-S3 определяет свой по умолчанию
#undef PIN_NEOPIXEL
#endif
#define PIN_NEOPIXEL        14
#define NEOPIXEL_COUNT      8

// ---------------------------------------------------------------------------
//  Порт QWIIC/Grove (внешние модули). Функция пинов зависит от подключённого
//  модуля: GPS — UART; NRF24 — CE/CSN (на общей шине SPI).
// ---------------------------------------------------------------------------
#define PIN_GPS_TX          43
#define PIN_GPS_RX          44
#define PIN_NRF_CE          43
#define PIN_NRF_CSN         44
#define PIN_IBUTTON         44      // 1-Wire (Dallas) пробник на порт QWIIC

// ---------------------------------------------------------------------------
//  Микрофон (PDM) и динамик (NS4168, I2S)
// ---------------------------------------------------------------------------
#define PIN_MIC_CLK         39
#define PIN_MIC_DATA        42
#define PIN_SPK_BCLK        46
#define PIN_SPK_WCLK        40
#define PIN_SPK_DOUT        7
#define PIN_SPK_MCLK        39
