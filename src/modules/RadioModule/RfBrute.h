// ============================================================================
//  RfBrute.h — Таблица протоколов перебора фикс-кодов (тайминги из Bruce)
//
//  Соглашение по таймингам: положительное = HIGH (несущая вкл), отрицательное
//  = LOW (пауза), в микросекундах. Каждый кадр: pilot + биты (MSB-first) + stop,
//  повтор N раз. Это «качественный» перебор — открывает реальные шлагбаумы,
//  в отличие от сплошной последовательности де Брёйна.
// ============================================================================
#pragma once
#include <stdint.h>

struct BruteProtocol {
    const char* name;
    int bits;
    int zero[2];   // bit '0'
    int one[2];    // bit '1'
    int pilot[2];  // преамбула/синхро (0,0 — нет)
    int stop[2];   // стоп-бит (0,0 — нет)
};

static constexpr BruteProtocol brute_protocols[] = {
    {"Came 12bit",       12, {-320, 640},  {-640, 320},  {-11520, 320}, {0, 0}       },
    {"Nice 12bit",       12, {-700, 1400}, {-1400, 700}, {-25200, 700}, {0, 0}       },
    {"Ansonic 12bit",    12, {-1111, 555}, {-555, 1111}, {-19425, 555}, {0, 0}       },
    {"Holtek 12bit",     12, {-870, 430},  {-430, 870},  {-15480, 430}, {0, 0}       },
    {"Linear 10bit",     10, {500, -1500}, {1500, -500}, {0, 0},        {500, -21500}},
    {"Chamberlain 9bit", 9,  {-870, 430},  {-430, 870},  {0, 0},        {-3000, 1000}},
};

static constexpr int BRUTE_PROTOCOL_COUNT =
    sizeof(brute_protocols) / sizeof(brute_protocols[0]);
