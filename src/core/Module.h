// ============================================================================
//  Module.h — Базовый интерфейс модуля VARSYS
//
//  Каждая подсистема (UI, ввод, радио CC1101, ИК, NFC, настройки...)
//  реализует этот интерфейс и регистрируется в ModuleManager.
//  Это и есть основа модульности прошивки.
// ============================================================================
#pragma once
#include <Arduino.h>

class IModule {
public:
    virtual ~IModule() = default;

    // Уникальное имя модуля (используется как тег логов и для поиска).
    virtual const char* name() const = 0;

    // Однократная инициализация при старте системы.
    // Возврат false означает фатальную ошибку модуля.
    virtual bool init() = 0;

    // Вызывается после успешной инициализации всех модулей.
    virtual void start() {}

    // Периодический вызов из главного цикла. now — millis().
    virtual void update(uint32_t now) {}

    // Корректное завершение работы (перезагрузка, сон).
    virtual void stop() {}

    // Включён ли модуль в цикл обновления.
    bool enabled = true;
};
